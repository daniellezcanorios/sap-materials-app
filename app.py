import streamlit as st
import pandas as pd
import re
from io import BytesIO

# === Configuración de la página ===
st.set_page_config(
    page_title="Procesador de Materiales Inventario Físico SAP Almacenes EPM",
    page_icon="📦",
    layout="wide"
)

# === Título principal ===
st.title("📦 Procesador de Materiales Inventario Físico SAP Almacenes EPM")
st.markdown(
    """
    Bienvenido al **Procesador de Materiales**.
    Esta aplicación permite cargar un archivo Excel exportado desde SAP,
    generar actas de inventario y descargar un Excel con los materiales de
    **Stock especial Q (Proyectos)**, separados por **Elemento PEP**,
    ordenados por **Ubicación** y luego por **Material**.
    """
)

# === Subir archivo Excel ===
archivo = st.file_uploader("📂 Subir archivo Excel de materiales SAP", type=["xlsx"])

if archivo is not None:
    df = pd.read_excel(archivo)

    # Normalizar columnas
    df['Ubicación'] = df['Ubicación'].fillna("").astype(str)
    df['Tipo de stock especial'] = df['Tipo de stock especial'].fillna("")

    # === Validación de tipos de stock especiales distintos de "" y Q ===
    df_invalidos = df[~df['Tipo de stock especial'].isin(["","Q"])]
    st.subheader("⚠️ Validación de materiales")
    if not df_invalidos.empty:
        st.dataframe(df_invalidos[['Centro','Almacén','Descripción de almacén','Tipo de stock especial','Material']])
    else:
        st.dataframe(pd.DataFrame([{
            'Centro': '0',
            'Almacén': '0',
            'Descripción de almacén': '0',
            'Tipo de stock especial': '0',
            'Material': '0'
        }]))

    # === Mostrar Actas ===
    st.subheader("📊 Actas generadas")
    actas = []
    contador_acta = 1
    mapa_titulos = {"": "Material en Stock Regular", "Q": "Material de Proyectos"}

    for tipo in ["", "Q"]:
        df_tipo = df[df['Tipo de stock especial'] == tipo]
        for almacen, grupo in df_tipo.groupby('Almacén'):
            desc_almacen = grupo['Descripción de almacén'].iloc[0]
            nombre_acta = f"Acta {contador_acta} – {mapa_titulos[tipo]} | Almacén {almacen} ({desc_almacen})"
            contador_acta += 1

            claves = ['Centro'] + (['Elemento PEP'] if tipo=="Q" else [])
            resumen = grupo.groupby(claves).agg(
                Cantidad=('Material','size'),
                Stock_libre=('Stock de libre utilización','count'),
                Stock_bloqueado=('Stock bloqueado','count'),
                Valor_libre=('Valor de stock de libre utilización','sum'),
                Valor_bloqueado=('Valor actual de stock bloqueado','sum'),
                Ubicacion_desde=('Ubicación', lambda x: min([v for v in x if v != ""], default="")),
                Ubicacion_hasta=('Ubicación', lambda x: max([v for v in x if v != ""], default="")),
                Ubicaciones_vacias=('Ubicación', lambda x: (x=="").sum())
            ).reset_index()

            # Insertar columnas al lado de Centro
            resumen.insert(1, 'Almacén', almacen)
            resumen.insert(2, 'Descripción de almacén', desc_almacen)
            resumen.insert(3, 'Tipo de stock especial', "Vacío" if tipo=="" else "Q")

            # Crear fila TOTAL
            fila_total = {
                'Centro': 'TOTAL',
                'Almacén': almacen,
                'Descripción de almacén': desc_almacen,
                'Tipo de stock especial': "Vacío" if tipo=="" else "Q",
                'Cantidad': resumen['Cantidad'].sum(),
                'Stock_libre': resumen['Stock_libre'].sum(),
                'Stock_bloqueado': resumen['Stock_bloqueado'].sum(),
                'Valor_libre': resumen['Valor_libre'].sum(),
                'Valor_bloqueado': resumen['Valor_bloqueado'].sum(),
                'Ubicacion_desde': resumen['Ubicacion_desde'].min(),
                'Ubicacion_hasta': resumen['Ubicacion_hasta'].max(),
                'Ubicaciones_vacias': resumen['Ubicaciones_vacias'].sum()
            }
            resumen.loc['TOTAL'] = fila_total

            st.markdown(f"### {nombre_acta}")
            st.dataframe(resumen.style.format({
                'Stock_libre': '{:.2f}',
                'Stock_bloqueado': '{:.2f}',
                'Valor_libre': '${:,.2f}',
                'Valor_bloqueado': '${:,.2f}'
            }))

    # === Exportar Excel con hojas por Elemento PEP (solo Stock especial Q) ===
    st.subheader("💾 Descarga de Excel por Almacén (Stock especial Q)")
    for almacen, grupo in df[df['Tipo de stock especial'] == "Q"].groupby('Almacén'):
        nombre_archivo = f"Materiales_Por_Elemento_PEP_{almacen}.xlsx"
        output = BytesIO()

        with pd.ExcelWriter(output, engine="openpyxl") as writer:
            for pep, subgrupo in grupo.groupby("Elemento PEP"):
                subgrupo_ordenado = subgrupo.sort_values(by=['Ubicación','Material'], ascending=[True, True])
                nombre_hoja = re.sub(r'[\[\]\\/:*?"<>|]', '-', str(pep))[:31]
                subgrupo_ordenado.to_excel(writer, sheet_name=nombre_hoja, index=False)

        st.download_button(
            label=f"⬇️ Descargar Excel para Almacén {almacen}",
            data=output.getvalue(),
            file_name=nombre_archivo,
            mime="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
        )
